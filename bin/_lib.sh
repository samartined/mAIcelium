#!/usr/bin/env bash
# Shared functions for mAIcelium scripts.
# Source this file: source "$(dirname "$0")/_lib.sh"

# ── Load mesh conventions ────────────────────────────────────────────────────
# Reads project directory naming conventions from mesh/conventions.json.
# Sets MESH_PROJECT_* variables used by all scripts.
_load_conventions() {
  local root="$1"
  local conv="$root/mesh/conventions.json"
  if [ -f "$conv" ]; then
    eval "$(python3 - "$conv" <<'PYEOF'
import json, sys
d = json.load(open(sys.argv[1]))
print("MESH_PROJECT_DATA_DIR=" + repr(d.get("project_data_dir", ".cursor")))
print("MESH_PROJECT_DATA_SUBDIRS=" + repr(" ".join(d.get("project_data_subdirs", ["plans","bitacora","config","agents","docs"]))))
print("MESH_PROJECT_RULES_SUBDIR=" + repr(d.get("project_rules_subdir", "rules")))
print("MESH_PROJECT_SKILLS_SUBDIRS=" + repr(" ".join(d.get("project_skills_subdirs", ["skills","skills-cursor"]))))
PYEOF
)"
  else
    MESH_PROJECT_DATA_DIR=".cursor"
    MESH_PROJECT_DATA_SUBDIRS="plans bitacora config agents docs"
    MESH_PROJECT_RULES_SUBDIR="rules"
    MESH_PROJECT_SKILLS_SUBDIRS="skills skills-cursor"
  fi
}

# ── Load mesh layers ─────────────────────────────────────────────────────────
# Reads the mesh_layers section from WORKSPACE.md.
# Returns a JSON array of layer objects: [{name, path, client?, repo?}, ...]
_load_mesh_layers() {
  local root="$1"
  python3 - "$root" <<'PYEOF'
import sys, os, json

root = sys.argv[1]
wf = os.path.join(root, "WORKSPACE.md")
layers = []

try:
    with open(wf) as f:
        content = f.read()

    in_layers = False
    current = None

    for line in content.splitlines():
        stripped = line.strip()

        if stripped == 'mesh_layers:':
            in_layers = True
            continue

        if not in_layers:
            continue

        # End of mesh_layers section: new top-level key
        if line and not line.startswith(' ') and not line.startswith('-') and stripped.endswith(':'):
            if current:
                layers.append(current)
                current = None
            break

        if stripped.startswith('- name:'):
            if current:
                layers.append(current)
            current = {'name': stripped.split(':', 1)[1].strip()}
        elif stripped.startswith('path:') and current is not None:
            current['path'] = os.path.expanduser(stripped.split(':', 1)[1].strip())
        elif stripped.startswith('client:') and current is not None:
            current['client'] = stripped.split(':', 1)[1].strip()
        elif stripped.startswith('repo:') and current is not None:
            current['repo'] = stripped.split(':', 1)[1].strip()

    if current:
        layers.append(current)

except Exception:
    pass

print(json.dumps(layers))
PYEOF
}

_regenerate_workspace_file() {
  local root="$1"
  local wsfile="$root/mAIcelium.code-workspace"

  python3 -c '
import json, os, sys

root = sys.argv[1]
projects_dir = os.path.join(root, "projects")

folders = [{"path": ".", "name": "mAIcelium"}]

if os.path.isdir(projects_dir):
    for entry in sorted(os.listdir(projects_dir)):
        link = os.path.join(projects_dir, entry)
        if os.path.islink(link) and os.path.isdir(link):
            real = os.path.realpath(link)
            folders.append({"path": real, "name": entry})

wsfile = os.path.join(root, "mAIcelium.code-workspace")
existing = {}
if os.path.isfile(wsfile):
    try:
        with open(wsfile) as f:
            existing = json.load(f)
    except (json.JSONDecodeError, OSError):
        pass

existing["folders"] = folders
existing.setdefault("settings", {})

with open(wsfile, "w") as f:
    json.dump(existing, f, indent=2, ensure_ascii=False)
    f.write("\n")
' "$root"
}

_regenerate_claude_context() {
  local root="$1"
  local outfile="$root/.claude/projects-context.md"

  mkdir -p "$root/.claude"

  _load_conventions "$root"
  local mesh_layers
  mesh_layers="$(_load_mesh_layers "$root")"

  {
    echo "<!-- AUTO-GENERATED by mAIcelium scripts. Do not edit manually. -->"
    echo "# mAIcelium Agent Context"
    echo ""

    # ── Workspace rules (mesh/rules/) ──────────────────────────────────────────
    echo "## Workspace Rules"
    echo ""
    for rule in "$root"/mesh/rules/*.mdc; do
      [ -f "$rule" ] || continue
      echo "### $(basename "$rule" .mdc)"
      echo ""
      sed -n '/^---$/,/^---$/!p' "$rule"
      echo ""
    done

    # ── Active projects ──────────────────────────────────────────────────────
    echo "## Active Projects"
    echo ""

    local found_projects=false

    for project_link in "$root"/projects/*/; do
      [ -d "$project_link" ] || continue
      found_projects=true
      local pname
      pname=$(basename "$project_link")
      local repo_path
      repo_path=$(realpath "$project_link")

      echo "### $pname"
      echo ""

      # Find the mesh layer path for this project (client match)
      local layer_rules_dir layer_skills_dir
      layer_rules_dir=$(LAYERS_JSON="$mesh_layers" python3 - "$pname" <<'PYEOF'
import json, sys, os
layers = json.loads(os.environ['LAYERS_JSON'])
for l in layers:
    if l.get('client') == sys.argv[1]:
        p = l.get('path', '')
        print(os.path.join(p, 'rules') if p else '')
        sys.exit(0)
print('')
PYEOF
)
      layer_skills_dir=$(LAYERS_JSON="$mesh_layers" python3 - "$pname" <<'PYEOF'
import json, sys, os
layers = json.loads(os.environ['LAYERS_JSON'])
for l in layers:
    if l.get('client') == sys.argv[1]:
        p = l.get('path', '')
        print(os.path.join(p, 'skills') if p else '')
        sys.exit(0)
print('')
PYEOF
)

      # Rules: project-native + mesh layer
      local has_rules=false
      for rules_dir in "$repo_path/$MESH_PROJECT_DATA_DIR/$MESH_PROJECT_RULES_SUBDIR" "$layer_rules_dir"; do
        [ -n "$rules_dir" ] && [ -d "$rules_dir" ] || continue
        for rule in "$rules_dir"/*; do
          [ -f "$rule" ] || continue
          if [ "$has_rules" = false ]; then
            echo "#### Rules"
            echo ""
            has_rules=true
          fi
          local rulename
          rulename=$(basename "$rule")
          echo "##### $rulename"
          echo ""
          sed -n '/^---$/,/^---$/!p' "$rule"
          echo ""
        done
      done

      # Skills: project-native + mesh layer
      local has_skills=false
      local _sdirs=()
      for _s in $MESH_PROJECT_SKILLS_SUBDIRS; do
        _sdirs+=("$repo_path/$MESH_PROJECT_DATA_DIR/$_s")
      done
      [ -n "$layer_skills_dir" ] && _sdirs+=("$layer_skills_dir")
      for skills_dir in "${_sdirs[@]}"; do
        [ -d "$skills_dir" ] || continue
        for skill_dir in "$skills_dir"/*/; do
          [ -d "$skill_dir" ] || continue
          local skillname
          skillname=$(basename "$skill_dir")
          local skill_file="$skill_dir/SKILL.md"
          [ -f "$skill_file" ] || continue
          if [ "$has_skills" = false ]; then
            echo "#### Skills"
            echo ""
            has_skills=true
          fi
          echo "##### $skillname"
          echo ""
          sed -n '/^---$/,/^---$/!p' "$skill_file"
          echo ""
        done
      done

      # List project data directories
      local has_data=false
      for data_dir in $MESH_PROJECT_DATA_SUBDIRS; do
        local full_data_dir="$repo_path/$MESH_PROJECT_DATA_DIR/$data_dir"
        [ -d "$full_data_dir" ] || continue
        if [ "$has_data" = false ]; then
          echo "#### Project Data (accessible via symlink)"
          echo ""
          has_data=true
        fi
        echo "- \`projects/$pname/$MESH_PROJECT_DATA_DIR/$data_dir/\`"
      done
      [ "$has_data" = true ] && echo ""

      if [ "$has_rules" = false ] && [ "$has_skills" = false ]; then
        echo "_No rules or skills found for this project._"
      fi
      echo ""
    done

    if [ "$found_projects" = false ]; then
      echo "_No active projects._"
    fi
  } > "$outfile"
}
